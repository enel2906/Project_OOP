package Model;

public class Wars {
    private int id;
    private String ten;
    private String thuLinh;
    private String giac;
    private String trieuDai;
    private String giaiDoan;
    private String ketQua;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }
    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getThuLinh() {
        return thuLinh;
    }
    public void setThuLinh(String thuLinh) {
        this.thuLinh = thuLinh;
    }

    public String getGiac() {
        return giac;
    }
    public void setGiac(String giac) {
        this.giac = giac;
    }

    public String getTrieuDai() {
        return trieuDai;
    }
    public void setTrieuDai(String trieuDai) {
        this.trieuDai = trieuDai;
    }

    public String getGiaiDoan() {
        return giaiDoan;
    }
    public void setGiaiDoan(String giaiDoan) {
        this.giaiDoan = giaiDoan;
    }

    public String getKetQua() {
        return ketQua;
    }
    public void setKetQua(String ketQua) {
        this.ketQua = ketQua;
    }
}
